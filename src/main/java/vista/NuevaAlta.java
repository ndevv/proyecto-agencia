    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:
       
        //Cliente
   
        if (jTabbedAlta.getSelectedIndex() == 0) {
            System.out.println("Cliente");
            //Agregar "T" a los datos que no son de tipo String
            //Crear todas las variables de tipo String
            if (jCheckBoxPasajeroCliente.isSelected()) {
            
                int anio = Integer.parseInt(jTextFieldAnioCliente.getText());
                calculoMayoria(anio);
            }
            String docClienteT = jTextFieldDocCliente.getText();
            String nomCliente = jTextFieldNomCliente.getText(); 
            String apeCliente  = jTextFieldApeCliente.getText();
            //diaCliente combobox
            //mesCliente combobox
            String anioClienteT = jTextFieldAnioCliente.getText();
            String nacCliente = jTextFieldNacCliente.getText();
            String telClienteT = jTextFieldTelCliente.getText();
            String rutCliente = jTextFieldRutCliente.getText();
            String calleCliente = jTextFieldCalleCliente.getText();
            String nPuertaClienteT = jTextFieldNpuertaCliente.getText();
            String barrioCliente = jTextFieldBarrioCliente.getText();
            
            if(docClienteT.isBlank() || nomCliente.isBlank() || apeCliente.isBlank()
                    || anioClienteT.isBlank() || nacCliente.isBlank() || telClienteT.isBlank()
                    || rutCliente.isBlank() || calleCliente.isBlank() || nPuertaClienteT.isBlank() || barrioCliente.isBlank()) {
                JOptionPane.showMessageDialog(null, "Error - debe llenar todos los campos");
            }else{
                long documento =  Long.parseLong(docClienteT);
                //String nomCliente
                //String apeCliente
                int dia = jComboBoxDiaCliente.getSelectedIndex() + 1;
                int mes = jComboBoxMesCliente.getSelectedIndex() + 1;
                int anio = Integer.parseInt(anioClienteT);
                //String nacCliente
                long telefono = Long.parseLong(telClienteT);
                //String rutCliente
                //String calleCliente
                int npuerta = Integer.parseInt(nPuertaClienteT);
                //String barrioCliente
                
                Principal.crearCliente(documento, nomCliente, apeCliente, dia, mes, anio, nacCliente, 
                        telefono, rutCliente, calleCliente, npuerta, barrioCliente);
                Cliente nuevoCliente = Principal.returnCliente();
                
                MenuPrincipal.jTextArea1.setText(nuevoCliente.toString());
                
            }
        }
        if (jTabbedAlta.getSelectedIndex() == 1) {
            System.out.println("Pasajero");
        }
        if (jTabbedAlta.getSelectedIndex() == 2) {
            System.out.println("Destino");
        }
        if (jTabbedAlta.getSelectedIndex() == 3) {
            System.out.println("Hotel");
        }
        if (jTabbedAlta.getSelectedIndex() == 4) {
            System.out.println("Guía");
        }
        if (jTabbedAlta.getSelectedIndex() == 5) {
            System.out.println("Chofer");
        }
        if (jTabbedAlta.getSelectedIndex() == 6) {
            System.out.println("Excursión");
        }

    }//GEN-LAST:event_jButtonAceptarActionPerformed

