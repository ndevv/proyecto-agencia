    private void letrasYespacios(java.awt.event.KeyEvent evt) {
        //El textField acepta sólo letras y espacios
        if (!(evt.getKeyChar() >= 65 && evt.getKeyChar() <= 90
                || evt.getKeyChar() >= 97 && evt.getKeyChar() <= 122
                || evt.getKeyChar() == 32)) {
            evt.consume();
        }
    }

    private void soloNumerosLimite(java.awt.event.KeyEvent evt, int ingresados, int limite) {
        //El textField acepta solo números y un límite de estos en específico
        if (!(evt.getKeyChar() >= 48 && evt.getKeyChar() <= 57)) {
            evt.consume();
        }
        if (ingresados >= limite) {
            evt.consume();
        }
    }
    
    private void calculoMayoria(int anio){
        //Se calculará la mayoría de edad basándose en la fecha actual y la edad
        //de la persona
       Calendar fechaHoy = Calendar.getInstance();
       int anioHoy = fechaHoy.get(Calendar.YEAR);
       int resultado = anioHoy-anio;
       if(resultado<18){
            JOptionPane.showMessageDialog(null, "Cliente-Pasajero menor de edad, deberá ser acompañado");
       }
    }

